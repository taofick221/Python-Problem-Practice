# tuple
my_tuple = ("apple", "banana", "cherry")
print(my_tuple)
print(my_tuple[1])  # Accessing the second item
print(my_tuple[2:5])  # Slicing the tuple
if "apple" in my_tuple:
    print("Yes, 'apple' is in the fruits tuple")
    print(my_tuple.index("apple"))  # Finding the index of 'apple'
    print(my_tuple.count("apple"))  # Counting occurrences of 'apple'
    print(my_tuple.count("banana"))  # Counting occurrences of 'banana'
    print(my_tuple.index("banana"))  # Finding the index of 'banana'



