# For Loops
fruit= ["apple","banana","cherry"]
for x in fruit:
  if x == "banana":
    continue
  print(x)


# Nested for loop
adj = ["red","big","tasty"]
for x in adj:
  for y in fruit:
    print(x,y)  
print() 


# The range() function
for x in range(6):  
    print(x)

print()

for x in range(2, 6):  
    print(x)

print()


