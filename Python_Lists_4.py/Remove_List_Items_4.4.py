# Remove Specified Item
thislist = ["apple", "banana", "cherry"]
thislist.remove("banana")  
print(thislist)


# Remove the second item
thislist = ["apple", "banana", "cherry"]
thislist.pop(1)
print(thislist)



# Delete the entire list

thislist = ["apple", "banana", "cherry"]
del thislist


# Clear the list content
thislist = ["apple", "banana", "cherry"]
thislist.clear()
print(thislist)

