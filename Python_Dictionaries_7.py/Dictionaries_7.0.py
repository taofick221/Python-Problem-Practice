# Dictionarys in Python
thisdict={
    "brand":"ford",
    "model":"mustang",  
    "year":1964 
}
print(thisdict)

# Print the "brand" value of the dictionary
thisdict={
    "brand":"ford", 
    "model":"mustang",  
    "year":1964
}
print(thisdict["brand"])

# Using the dict() method to make a dictionary
thisdict=dict(brand="ford", model="mustang", year=1964)
print(thisdict)


