# Change the "year" to 2018
thisdict={
    "brand":"ford",
    "model":"mustang",
    "year":1964 
}
thisdict["year"]=2018
print(thisdict)


# Update the "year" to 2020 using the update() method
thisdict={
    "brand":"ford",
    "model":"mustang",
    "year":1964 
}
thisdict.update({"year":2020})
print(thisdict)


